    /**
     * Agregar pedido a la lista de entregados.
     *
     * @param evt
     */
    private void btnEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntregarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tablaPedidosPorEntregar.getModel();

        if (modelo.getRowCount() > 0 && !(tablaPedidosPorEntregar.getSelectedRow() < 0)) {
            int i = tablaPedidosPorEntregar.getSelectedRow();

            Pedido Buscar = Inicio.ventanaCocina.PedidosParaEntrega;
            Pedido ant = null;

            while (Buscar != null && String.valueOf(Buscar.cod).equals(modelo.getValueAt(i, 0).toString()) == false) {
                ant = Buscar;
                Buscar = Buscar.link;
            }

            if (Inicio.ventanaCocina.PedidosEntregados == null) {
                Inicio.ventanaCocina.PedidosEntregados = new Pedido();
            }

            Pedido nuevoPedido = new Pedido(Buscar.tiempo);
            nuevoPedido.cod = Buscar.cod;
            nuevoPedido.Platos = Buscar.Platos;
            nuevoPedido.camarero = Buscar.camarero;
            nuevoPedido.mesa = Buscar.mesa;
            nuevoPedido.valortotal = Buscar.valortotal;
            Inicio.ventanaCocina.PedidosEntregados = Inicio.ventanaCocina.PedidosEntregados.addPedido(camarerosFrame, nuevoPedido); // Agregar pedido a la lista

            if (ant == null) {
                Inicio.ventanaCocina.PedidosParaEntrega = Inicio.ventanaCocina.PedidosParaEntrega.link;
            } else {
                ant.link = Buscar.link;
            }

            //modelo.removeRow(i);
            actualizarTablaPedidosPorEntregar(Inicio.ventanaCocina.PedidosParaEntrega);

            //Actualiza lista de pedidos por entregar en cocina
            if (Inicio.ventanaCocina.PedidosParaEntrega == null) {
                Inicio.ventanaCocina.PedidosParaEntrega = new Pedido();
                //Inicio.ventanaCocina.PedidosParaEntregarList.removeAll();
                Inicio.ventanaCocina.PedidosParaEntregarList.setModel(new DefaultListModel());
            } else {
                Inicio.ventanaCocina.PedidosParaEntrega.toJListByCod(Inicio.ventanaCocina.PedidosParaEntregarList);
            }

            //Guarda el archivo de pedidos a entregar
            Inicio.ventanaCocina.PedidosParaEntrega.toFile(camarerosFrame, Inicio.PedidosParaEntregar);
            //Guarda el archivo de pedidos entregados
            Inicio.ventanaCocina.PedidosEntregados.toFile(camarerosFrame, Inicio.PedidosEntregados);
            //Carga las mesas con pedidos entregados en la lista de las mismas en notificaciones
            Inicio.ventanaCocina.PedidosEntregados.toJListByCamarero(jlistPedidosEntregados, camarero);

        } else { // Si no seleccionó ningún pedido o no hay pedidos por entregar
            JOptionPane.showMessageDialog(pedidosEntregarFrame, "No ha seleccionado ningún pedido para entregar.", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEntregarActionPerformed

    /**
     * Facturar orden.
     *
     * @param evt Click sobre el boton Facturar.
     */
    private void btnFacturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFacturarActionPerformed
        if (jlistPedidosEntregados.getSelectedIndex() >= 0) {
            int elem = Integer.parseInt(jlistPedidosEntregados.getSelectedValue().substring(5, jlistPedidosEntregados.getSelectedValue().length()));
            Pedido Buscar = Inicio.ventanaCocina.PedidosEntregados;
            Pedido ant = null;

            //Busca el elemento seleccionado en la lista
            while (Buscar.cod != elem) {
                ant = Buscar;
                Buscar = Buscar.link;
            }

            Factura nuevaFactura = new Factura(Buscar);

            //Se añade la factura a la lista de facturas
            if (Inicio.ventanaCocina.Facturas == null) {
                Inicio.ventanaCocina.Facturas = new Factura();
            }
            Inicio.ventanaCocina.Facturas = Inicio.ventanaCocina.Facturas.addFactura(nuevaFactura);

            //Borra el pedido de la lista de pedidos por facturar
            if (ant == null) {
                Inicio.ventanaCocina.PedidosEntregados = Inicio.ventanaCocina.PedidosEntregados.link;
                if (Inicio.ventanaCocina.PedidosEntregados == null) {
                    Inicio.ventanaCocina.PedidosEntregados = new Pedido();
                    /*
                    * En vista de que no funciona esta instrucción 
                    * (jlistPedidosEntregados.removeAll();) para remover 
                    * todos los elementos de la lista se cambiará el modelo
                     */
                    jlistPedidosEntregados.setModel(new DefaultListModel());
                } else {
                    Inicio.ventanaCocina.PedidosEntregados.toJListByCamarero(jlistPedidosEntregados, camarero);
                }
            } else {
                ant.link = Buscar.link;
                Inicio.ventanaCocina.PedidosEntregados.toJListByCamarero(jlistPedidosEntregados, camarero);
            }

            //Actualiza pedidos entregados
            Inicio.ventanaCocina.PedidosEntregados.toFile(pedidosEntregarFrame, Inicio.PedidosEntregados);

            mostrarFactura(nuevaFactura);

            Inicio.ventanaCocina.Facturas.toFile(camarerosFrame, Inicio.FacturasFile);
            // TODO Corregir error al generar archivo de facturas
        }

    }//GEN-LAST:event_btnFacturarActionPerformed

    /**
     *
     * @param evt
     */
    private void camareroBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_camareroBoxItemStateChanged
        if (Inicio.ventanaCocina != null) {
            actualizarTablaPedidosPorEntregar(Inicio.ventanaCocina.PedidosParaEntrega);
            //Carga los pedidos del camarero respectivo en las notificaciones
            camarero = camareroBox.getSelectedIndex() + 1;
            Inicio.ventanaCocina.PedidosEntregados.toJListByCamarero(jlistPedidosEntregados, camarero);
            //Carga los pedidos de la mesa respectiva en su respectiva lista
            Inicio.ventanaCocina.Pedidos.toJListByMesa(Inicio.ventanaMenú.JListPedidosGenerados, 5 * (camarero - 1) + 1);
        }
        Inicio.ventanaMenú.mesaBox.removeAllItems();
        switch (camareroBox.getSelectedIndex() + 1) {
            case 1:
                for (int i = 1; i <= 5; i++) {
                    Inicio.ventanaMenú.mesaBox.addItem("Mesa_" + i);
                }
                break;
            case 2:
                for (int i = 6; i <= 10; i++) {
                    Inicio.ventanaMenú.mesaBox.addItem("Mesa_" + i);
                }
                break;
            case 3:
                for (int i = 11; i <= 15; i++) {
                    Inicio.ventanaMenú.mesaBox.addItem("Mesa_" + i);
                }
                break;
            case 4:
                for (int i = 16; i <= 20; i++) {
                    Inicio.ventanaMenú.mesaBox.addItem("Mesa_" + i);
                }
                break;

        }
    }//GEN-LAST:event_camareroBoxItemStateChanged

    /**
     * Mostrar factura.
     *
     * @param factura Factura que será mostrada.
     */
    public void mostrarFactura(Factura factura) {
        ventanaFactura.setLocationRelativeTo(null);
        ventanaFactura.setVisible(true);

        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };

        modelo.addColumn("Código");
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Total");
        modelo.addColumn("Arandelas");

        tablaProd.setModel(modelo);

        tablaProd.getColumnModel().getColumn(1).setPreferredWidth(180);
        tablaProd.getColumnModel().getColumn(1).setResizable(false);

        PlatoPedido p = factura.platos;
        while (p != null) {
            modelo.addRow(new Object[]{p.cod, p.nombre, p.cant, p.value * p.cant, p.cant * p.value * 0.29});
            p = p.link;
        }

        txtMESA.setText(factura.mesa + "");
        txtMESERO.setText(factura.camarero + "");
        txtTOTAL.setText("$ " + factura.total);
        txtCODIGO.setText("#" + factura.cod);
        txtFECHA.setText(factura.fecha_exp);
        txtTIEMPO.setText(String.valueOf(factura.tiempo) + " seg.");
    }

