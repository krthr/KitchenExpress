    /**
     * Actualizar la tabla de pedidos por entregar.
     *
     * @param PedidosEntrega es la lista de pedidos que se usará para actualizar
     * la tabla tablaPedidosPorEntregar
     */
    public void actualizarTablaPedidosPorEntregar(Pedido PedidosEntrega) {
        //System.out.println("Actualizando tabla de pedidos por entregar...");

        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }

        };
        tablaPedidosPorEntregar.setModel(modelo);
        modelo.addColumn("Cod");
        modelo.addColumn("Mesa");
        modelo.addColumn("Camarero");

        Pedido p = PedidosEntrega;
        boolean notif = false;

        if (p != null && p.camarero != 0) {
            while (p != null) {
                //System.out.println("Agregando fila");
                if (p.camarero == camarero) {
                    modelo.addRow(new Object[]{p.cod, p.mesa, p.camarero});
                    if (notif == false) {
                        notif = true;
                    }
                }
                p = p.link;
            }
        }
        // Si hay pedidos por entregar, mostrar notificación 
        if (notif) {
            mostrarNotificacion();
        } else {
            ocultarNotificacion();
        }
    }

